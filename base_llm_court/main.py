from court import LLMCourt


def main():
    # ケースをここに入れる
    case_text = """
株式会社BlueBridge（以下「原告」という。）は、データ収集および機械学習モデルの改善支援を行う企業である。株式会社DataField（以下「委託先」という。）は、データアノテーション業務等を受託する事業者である。

原告は、自社の機械学習モデルの精度向上のため、委託先に対してデータ収集およびラベル付け業務を委託することとした。契約締結にあたり、原告は契約書作成の効率化を目的として、株式会社LexiAIが提供する契約書作成支援システム「ContractForge」を利用した。

原告担当者は当該システムに対し、「業務委託契約」「準委任契約」「成果報酬型」「日本法準拠」と入力し、契約書案の生成を依頼した。

同システムにより生成された契約書案には、次の条項が含まれていた。

第一に、成果物は機械学習モデルの精度改善に資するデータセットとする旨の条項である。
第二に、報酬は業務の進捗に応じて支払う旨の条項である。
第三に、進捗の評価は合理的な基準に基づき双方協議の上決定する旨の条項である。

原告は当該契約書案を確認したうえで、委託先との間で同内容の契約を締結した。

その後、委託先は約二か月間にわたりデータ収集およびラベル付け作業を実施した。しかし、原告は納品されたデータの一部について品質が自社基準に達していないと判断し、報酬の全額支払いを拒否した。

これに対し委託先は、契約上「進捗に応じて支払う」と規定されている以上、作業が実施された時点で報酬支払義務が発生しており、品質評価を理由とする減額は認められないとして、未払報酬の支払いを求めている。

一方で原告は、成果物の内容が不明確であり、報酬支払条件も客観的基準を欠いているため、支払義務の範囲は限定されるべきであると主張している。

【証拠関係】

原告は、以下の証拠を提出している。
第一に、原告と委託先との間で締結された業務委託契約書である。
第二に、納品されたデータサンプルであり、ラベル付きの実データを含むものである。
第三に、原告社内において作成された品質評価レポートであり、納品データの一部に不備がある旨を指摘する内容である。
第四に、契約締結前に原告と委託先との間で交わされた電子メールであり、早期に契約を締結したい旨の記載が含まれている。

他方、委託先は、以下の証拠を提出している。
第一に、業務実施ログであり、作業時間および作業内容が記録されたものである。
第二に、納品データの一覧であり、委託業務の成果物を示すものである。
第三に、契約書条文の写しであり、「進捗に応じて報酬を支払う」とする条項を含むものである。
"""

    court = LLMCourt()
    result = court.run(case_text)

    print("\n===== FINAL OUTPUT =====\n")
    for i, h in enumerate(result["history"]):
        print(f"\n--- TURN {i} ---")
        print(f"[{h['role']}]")
        print(h["content"])
    print(result)


if __name__ == "__main__":
    main()